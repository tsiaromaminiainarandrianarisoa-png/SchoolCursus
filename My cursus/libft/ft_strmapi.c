#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	size_t	len;
	size_t	loc;

	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	str = malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	loc = 0;
	while (loc < len)
	{
		str[loc] = (*f)(loc, s[loc]);
		loc++;
	}
	str[loc] = '\0';
	return (str);
}
